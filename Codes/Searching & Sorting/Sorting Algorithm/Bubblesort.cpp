{
    boost;
    int arr[5] = {3, 2, 342, 1, 3};
    int n = 5;
    for (int i = 0; i < n; i++)
    {
        bool swapped = false;
        for (int j = 0; j < n - i - 1; j++)
        {
            if (arr[j] > arr[j + 1])
            {
                swap(arr[j], arr[j + 1]);
                swapped = true;
            }
        }
        if (!swapped)
            break;
    }
    for (int I : arr)
        cout << I << " ";
    return 0;
}

